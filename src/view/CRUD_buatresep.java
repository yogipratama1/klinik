public void tabelobat() {
        String[] judul = {"ID Obat", "Nama Obat", "Kegunaan", "Harga"};
        model = new DefaultTableModel(judul, 0);
        tblobat.setModel(model);
        String sql = "SELECT * FROM tbl_obat where nama_obat like '%" + tCari.getText() + "%'";

        try {
            rs = con.createStatement().executeQuery(sql);

            while (rs.next()) {
                String id = rs.getString("id_obat");
                String nama = rs.getString("nama_obat");
                String kegunaan = rs.getString("kegunaan");
                String harga = rs.getString("harga");

                String[] data = {id, nama, kegunaan, harga};
                model.addRow(data);
            }
        } catch (Exception e) {
            System.out.println(e);

        }
    }

    public void tablearray() {

        //mengambil data periode
        int i = tblobat.getSelectedRow();
        String idobat = model.getValueAt(i, 0).toString();
        String nmobat = model.getValueAt(i, 1).toString();
        String harga = model.getValueAt(i, 3).toString();

        DefaultTableModel dataModel = (DefaultTableModel) tblresep.getModel();
        List list = new ArrayList<>();
        tblresep.setAutoCreateColumnsFromModel(true);
        list.add(idobat);
        list.add(nmobat);
        list.add(harga);
        dataModel.addRow(list.toArray());

    }

    public void resetform() {
        DefaultTableModel dataModel = (DefaultTableModel) tblresep.getModel();
        if (tblresep.getRowCount() > 0) {
            for (int i = tblresep.getRowCount() - 1; i > -1; i--) {
                dataModel.removeRow(i);
            }
        }
    }
