    private void btnsimpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnsimpanActionPerformed

        String idpendaftaran = tidpendaftaran.getText();
        int jumlah_baris = tblresep.getRowCount();
        if (jumlah_baris == 0) {
            JOptionPane.showMessageDialog(null, "Masukan data dengan benar!!");
            resetform();

        } else {

            try {
                int i = 0;
                while (i < jumlah_baris) {
                    con.createStatement().executeUpdate("insert into tbl_resep(id_pendaftaran,id_obat,harga_obat)"
                            + " Values("
                            + "'" + idpendaftaran + "','"
                            + "" + tblresep.getValueAt(i, 0) + "','"
                            + "" + tblresep.getValueAt(i, 2) + "')");
                    i++;

                }
                con.createStatement().executeUpdate("Insert into tbl_pembayaran(id_pendaftaran,status_resep)values('"+idpendaftaran+"','belum dibuat')");
                JOptionPane.showMessageDialog(null, "Resep Berhasil Disimpan!!");
                btnsimpan.setEnabled(false);

            } catch (Exception e) {
                JOptionPane.showMessageDialog(null, "Gagal Menyimpan!!" + e);
            }
        }
    }//GEN-LAST:event_btnsimpanActionPerformed

