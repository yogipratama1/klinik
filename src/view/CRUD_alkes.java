    private void btnsimpanresepActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnsimpanresepActionPerformed

        String idpendaftaran = tidpendaftaran.getText();

        int jumlah_baris = tblresep.getRowCount();
        if (jumlah_baris == 0) {
            JOptionPane.showMessageDialog(null, "Masukan data dengan benar!!");
            resetform();

        } else {

            try {

                int i = 0;
                while (i < jumlah_baris) {

                    con.createStatement().executeUpdate("insert into tbl_rialkes"
                            + " Values("
                            + "'" + idpendaftaran + "','"
                            + "" + tblresep.getValueAt(i, 0) + "','"
                            + "" + ttanggal.getText() + "','"
                            + "" + tblresep.getValueAt(i, 2) + "','"
                            + "" + tblresep.getValueAt(i, 2) + "','"
                            + "" + tblresep.getValueAt(i, 3) + "')");
                    con.createStatement().executeUpdate("insert into tbl_riresep"
                            + " Values("
                            + "'" + idpendaftaran + "','"
                            + "" + tblresep.getValueAt(i, 0) + "','"
                            + "" + ttanggal.getText() + "','"
                            + "" + tblresep.getValueAt(i, 2) + "','"
                            + "" + tblresep.getValueAt(i, 3) + "','"
                            + "Alkes')");

                    con.createStatement().executeUpdate("update tbl_obat set stok_obat='" + tblresep.getValueAt(i, 4) + "' where id_obat='" + tblresep.getValueAt(i, 0) + "'");
                    //upadte laporan harian & bulanan
                    String sql = "select * from tbl_ripelayanan where id_pendaftaran='" + idpendaftaran + "'";
                    try {
                        rs = con.createStatement().executeQuery(sql);
                        while (rs.next()) {
                            String umumbpjs = rs.getString("umum_bpjs");

                            if (umumbpjs.equals("umum")) {

                                con.createStatement().executeUpdate("update tbl_laporanobatharian set"
                                        + " ri_umum=ri_umum+" + tblresep.getValueAt(i, 2) + ",total_pemakaian=total_pemakaian+" + tblresep.getValueAt(i, 2) + ""
                                        + " where id_obat='" + tblresep.getValueAt(i, 0) + "'");
                                con.createStatement().executeUpdate("update tbl_laporanobatbulanan set"
                                        + " total_riumum=total_riumum+" + tblresep.getValueAt(i, 2) + ",total_pemakaian=total_pemakaian+" + tblresep.getValueAt(i, 2) + ""
                                        + " where id_obat='" + tblresep.getValueAt(i, 0) + "'");

                            } else {

                                con.createStatement().executeUpdate("update tbl_laporanobatharian set"
                                        + " ri_bpjs=ri_bpjs+" + tblresep.getValueAt(i, 2) + ",total_pemakaian=total_pemakaian+" + tblresep.getValueAt(i, 2) + " "
                                        + "where id_obat='" + tblresep.getValueAt(i, 0) + "'");
                                con.createStatement().executeUpdate("update tbl_laporanobatbulanan set"
                                        + " total_ribpjs=total_ribpjs+" + tblresep.getValueAt(i, 2) + ",total_pemakaian=total_pemakaian+" + tblresep.getValueAt(i, 2) + ""
                                        + " where id_obat='" + tblresep.getValueAt(i, 0) + "'");
                            }

                        }
                    } catch (Exception e) {
                        JOptionPane.showMessageDialog(null, "Input ke tbl_laporan_obatharian gagal ! " + e);

                    }
                    // batas update laporanharian
                    i++;

                }
                JOptionPane.showMessageDialog(null, "Resep Berhasil Disimpan!!");
                resetformobat();
                resetform();

                tambahlist.dispose();

            } catch (Exception e) {
                JOptionPane.showMessageDialog(null, "Gagal Menyimpan!!" + e);
            }
        }
    }//GEN-LAST:event_btnsimpanresepActionPerformed

