    public void ID_AUTO() {
        try {
            String sql = "Select max(right(id_tindakan,2)) as no_iduser from tbl_tindakan";
            rs = con.createStatement().executeQuery(sql);
            while (rs.next()) {
                if (rs.first() == false) {
                    tidpenunjang.setText("P-001");

                } else {
                    rs.last();
                    int autoid = rs.getInt(1) + 1;
                    String nomor = String.valueOf(autoid);

                    if (autoid <= 99) {
                        tidpenunjang.setText("P-00" + autoid);
                    } else if (autoid > 99 && autoid <= 999) {
                        tidpenunjang.setText("P-0" + autoid);
                    }
                }
            }
        } catch (Exception e) {
            System.out.println(e);
            JOptionPane.showMessageDialog(null, "masukan data dengan benar!!");
        }
    }

