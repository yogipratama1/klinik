public void tabelpenunjang() {
        String[] judul = {"ID Tindakan", "Nama Tindakan", "Tarif"};
        model = new DefaultTableModel(judul, 0);
        tblpenunjang.setModel(model);
        String sql = "SELECT * FROM tbl_tindakan where nama_tindakan like '%" + tcari.getText() + "%'";

        try {
            rs = con.createStatement().executeQuery(sql);

            while (rs.next()) {
                String id = rs.getString("id_tindakan");

                String nama = rs.getString("nama_tindakan");
                String tarif = rs.getString("tarif");

                String[] data = {id, nama, tarif};
                model.addRow(data);
            }
        } catch (Exception e) {
            System.out.println(e);

        }
    }

