 private void tabelAdmin() {
        String[] judul = {"Kode RM", "Nama Pasien", "Tanggal", "PLT datang", "PLT Pulang", "Keterangan"};
        model = new DefaultTableModel(judul, 0);
        tbladmin.setModel(model);
        String sql = "SELECT *, tbl_rm.nama_pasien FROM tbl_datasakit"
                + " inner join tbl_rm using(kode_rm) where kode_icd ='A90' and (tanggal_periksa BETWEEN '" + tawal.getText() + "' AND '" + takhir.getText() + "')";

        try {
            rs = con.createStatement().executeQuery(sql);

            while (rs.next()) {
                String id = rs.getString("kode_rm");
                String nama = rs.getString("nama_pasien");
                String user = rs.getString("tanggal_periksa");
                String pass = rs.getString("plt_datang");
                String Telp = rs.getString("plt_pulang");
                String level = rs.getString("keterangan");

                String[] data = {id, nama, user, pass, Telp, level};
                model.addRow(data);
            }
        } catch (Exception e) {
            System.out.println(e);

        }
    }

    public void ID_AUTO() {
        try {
            String sql = "Select max(right(id_user,2)) as no_iduser from tbl_user";
            rs = con.createStatement().executeQuery(sql);
            while (rs.next()) {
                if (rs.first() == false) {
                    tiduser.setText("U-01");

                } else {
                    rs.last();
                    int autoid = rs.getInt(1) + 1;
                    String nomor = String.valueOf(autoid);

                    for (int a = 0; a < 1; a++) {
                        nomor = "0" + nomor;
                    }
                    tiduser.setText("U-" + nomor);
                }
            }
        } catch (Exception e) {
            System.out.println(e);
            JOptionPane.showMessageDialog(null, "masukan data dengan benar!!");
        }
    }

