    private void btnsimpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnsimpanActionPerformed

        String id = tiduser.getText();
        String pltpulang = tpltpulang.getText();
        String pltdatang = tpltdatang.getText();
        String nama = tNama.getText();
        String ket = telp.getText();
        String tanggal = model.getValueAt(tbladmin.getSelectedRow(), 2).toString();
        System.out.println(tanggal);

        if (nama.length() == 0) {
            JOptionPane.showMessageDialog(null, "Masukan data dengan benar!!");
            Resetform();

        } else {

            try {
                con.createStatement().executeUpdate("update tbl_datasakit set plt_datang='" + pltdatang + "',"
                        + "plt_pulang='" + pltpulang + "',keterangan='" + ket + "' where kode_rm='" + id + "' and"
                        + " tanggal_periksa='" + tanggal + "'");

                JOptionPane.showMessageDialog(null, "Data Berhasil Diupdate");
                Resetform();
            } catch (Exception ex) {
                System.out.println("" + ex);
                JOptionPane.showMessageDialog(null, "Gagal !! Isi Form Dengan Benar ");
            }

        }


    }//GEN-LAST:event_btnsimpanActionPerformed

    private void btnhapusActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnhapusActionPerformed
        int keputusan = JOptionPane.showConfirmDialog(this, "apakah anda yakinmenghapus data ini? ");
        switch (keputusan) {
            case JOptionPane.YES_OPTION:
                try {
                    con.createStatement().executeUpdate("delete from tbl_datasakit where kode_rm = '" + model.getValueAt(tbladmin.getSelectedRow(), 0) + "'"
                            + " and tanggal_periksa='" + model.getValueAt(tbladmin.getSelectedRow(), 2) + "'");
                    JOptionPane.showMessageDialog(null, "Berhasil");

                } catch (Exception e) {
                    JOptionPane.showMessageDialog(null, "Gagal !!!");

                }
                Resetform();
        }
    }//GEN-LAST:event_btnhapusActionPerformed

