    public void cetaklaporan() throws JRException {
        DateFormat df = new SimpleDateFormat("yyyy-MM-dd");

        String bulan = cbbulan.getSelectedItem().toString();
        int jmlpasien = tbladmin.getRowCount();
        String jumlahpasien = String.valueOf(jmlpasien);
        JasperDesign jd = JRXmlLoader.load(getClass().getResourceAsStream("/laporan/laporan_db.jrxml"));
        JRDesignQuery query = new JRDesignQuery();
        HashMap param = new HashMap();
        param.put("bulan", bulan);
        param.put("jumlahpasien", jumlahpasien);
        query.setText("SELECT *, tbl_rm.* FROM tbl_datasakit"
                + " inner join tbl_rm using(kode_rm) where kode_icd ='A90' "
                + " and (tanggal_periksa BETWEEN '" + tawal.getText() + "' AND '" + takhir.getText() + "')");
        JasperReport jr = JasperCompileManager.compileReport(jd);
        JasperPrint jp = JasperFillManager.fillReport(jr, param, con);
        JasperViewer.viewReport(jp, false);

    }

