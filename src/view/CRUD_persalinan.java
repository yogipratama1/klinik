    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        int i = tbllist.getSelectedRow();
        String id = modellist.getValueAt(i, 0).toString();
        int jumlah = Integer.parseInt(JOptionPane.showInputDialog("Masukan Jumlah Tambahan !!"));

        try {
            String sql = "select * from tbl_ripersalinan where id_pendaftaran='" + tidpendaftaran.getText() + "'";
            rs = con.createStatement().executeQuery(sql);
            while (rs.next()) {
                int jmlawal = Integer.parseInt(rs.getString("jumlah_awalobat"));
                int hgtotal = Integer.parseInt(rs.getString("harga_totalobat"));

                int harga = hgtotal / jmlawal;
                int hargatotal = jumlah * harga;
                try {
                    con.createStatement().executeUpdate("update tbl_ripersalinan set jumlah_awalobat=jumlah_awalobat+'" + jumlah + "',jumlah_sisaobat=jumlah_sisaobat+'" + jumlah + "',harga_totalobat=harga_totalobat+'" + hargatotal + "' where id_obat='" + id + "' and id_pendaftaran='" + tidpendaftaran.getText() + "'");
                    con.createStatement().executeUpdate("update tbl_obat set stok_obat=stok_obat-'" + jumlah + "' where id_obat='" + id + "'");
                    JOptionPane.showMessageDialog(null, "Berhasil !!");
                    tampillistinjeksi();
                } catch (Exception e) {
                    System.out.println(e);
                    JOptionPane.showMessageDialog(null, "Gagal !! " + e);
                }
            }
        } catch (Exception e) {
            System.out.println(e);
            JOptionPane.showMessageDialog(null, "Gagal !! " + e);
        }
    }//GEN-LAST:event_jButton1ActionPerformed

