    public void ID_AUTO() {
        try {
            String sql = "Select max(right(id_pendaftaran,5)) as no_idtransaksi from tbl_ripelayanan";
            rs = con.createStatement().executeQuery(sql);
            while (rs.next()) {
                if (rs.first() == false) {
                    tidpendaftaran.setText("RI-00001");

                } else {
                    rs.last();
                    int autoid = rs.getInt(1) + 1;
                    if (autoid <= 99) {
                        tidpendaftaran.setText("RI-0000" + autoid);
                    } else if (autoid > 99 && autoid <= 999) {
                        tidpendaftaran.setText("RI-000" + autoid);
                    } else if (autoid > 999 ) {
                        tidpendaftaran.setText("RI-00" + autoid);
                    }
                }
            }
        } catch (Exception e) {
            System.out.println(e);
            JOptionPane.showMessageDialog(null, "Error ID Pendaftaran" + e);
        }
    }

