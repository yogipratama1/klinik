    private void submitActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_submitActionPerformed
        String user = In_username.getText();
        String pass = In_password.getText();

        try {
            Statement st = con.createStatement();
            String sql = "SELECT * FROM  tbl_user where username='" + user + "' and password='" + pass + "'";
            rs = st.executeQuery(sql);

            if (rs.next()) {
                String id = rs.getString("id_user");
                String nama = rs.getString("nama_user");
                String username = rs.getString("username");
                String level = rs.getString("level");

                //set user data session
                Usersession.setId(id);
                Usersession.setNama(nama);
                Usersession.setUsername(username);
                Usersession.setLevel(level);

                switch (level) {
                    case "admin": {
                        JOptionPane.showMessageDialog(null, "Selamat datang " + username + " !");
                        dashboard dsb = new dashboard();
                        dsb.dashAdmin();
                        dsb.setExtendedState(JFrame.MAXIMIZED_BOTH);
                        dsb.setVisible(true);
                        dispose();
                        break;
                    }
                    case "receptionist": {
                        JOptionPane.showMessageDialog(null, "Selamat datang " + username + " !");
                        dashboard dsb = new dashboard();
                        dsb.dashReceptionist();
                        dsb.setExtendedState(JFrame.MAXIMIZED_BOTH);
                        dsb.setVisible(true);
                        dispose();
                        break;
                    }
                    case "dokter": {
                        JOptionPane.showMessageDialog(null, "Selamat datang " + username + " !");
                        dashboard dsb = new dashboard();
                        dsb.dashDokter();
                        dsb.setExtendedState(JFrame.MAXIMIZED_BOTH);
                        dsb.setVisible(true);
                        dispose();
                        break;
                    }
                    case "apoteker": {
                        JOptionPane.showMessageDialog(null, "Selamat datang " + username + " !");
                        dashboard dsb = new dashboard();
                        dsb.dashApoteker();
                        dsb.setExtendedState(JFrame.MAXIMIZED_BOTH);
                        dsb.setVisible(true);
                        dispose();
                        break;
                    }
                    default:
                        break;
                }

            } else {
                JOptionPane.showMessageDialog(null, "Username atau password salah");
            }
        } catch (SQLException e) {
            System.out.println(e);
        }
    }//GEN-LAST:event_submitActionPerformed

