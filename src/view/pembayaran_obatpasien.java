public void tabelpasien() {
        String[] judul = {"ID Pendaftaran", "Nama Pasien", "Satus Resep","status Bayar"};
        model = new DefaultTableModel(judul, 0);
        tblpasien.setModel(model);
        String sql = "SELECT *,tbl_rm.nama_pasien,tbl_tagihan.* FROM tbl_pelayanan"
                + " inner join tbl_rm using(kode_rm)"
                + " inner join tbl_tagihan using(id_pendaftaran)"
                + " where status_bayar='belum bayar'"        
                + " ORDER BY tbl_pelayanan.id_pendaftaran DESC";

        try {
            rs = con.createStatement().executeQuery(sql);

            while (rs.next()) {
                String id = rs.getString("id_pendaftaran");
                String nama = rs.getString("nama_pasien");              
                String resep = rs.getString("status_resep");
                String bayar = rs.getString("status_bayar");

                String[] data = {id, nama,resep,bayar};
                model.addRow(data);
            }
        } catch (Exception e) {
            System.out.println(e);
            JOptionPane.showMessageDialog(null, "tabel pembayaran" + e);

        }

    }

