    public void cetaknota() throws Exception {

        try {
            String sql = "select sum(jumlah_tindakan) as jumlah_tindakan from tbl_tindakanrj where id_pendaftaran='" + tidpendaftaran.getText() + "'";
            rs = con.createStatement().executeQuery(sql);
            while (rs.next()) {
                String jmltindakan = rs.getString("jumlah_tindakan");
                System.out.println(jmltindakan);
                String tgl = ttanggal.getText();
                JasperDesign jd = JRXmlLoader.load(getClass().getResourceAsStream("/laporan/notaklinik.jrxml"));
                JRDesignQuery query = new JRDesignQuery();
                HashMap param = new HashMap();
                param.put("tanggal", tgl);
                param.put("jumlahtindakan", jmltindakan);
                query.setText("select tbl_rm.nama_pasien,tbl_tagihan.*,"
                        + "tbl_pembayaran.*,tbl_resep.*, tbl_obat.nama_obat"
                        + " from tbl_pelayanan "
                        + " inner join tbl_rm using(kode_rm) "
                        + " inner join tbl_tagihan using(id_pendaftaran) "
                        + " inner join tbl_resep using(id_pendaftaran) "
                        + " inner join tbl_pembayaran using(id_pendaftaran) "
                        + " inner join tbl_obat on tbl_obat.id_obat=tbl_resep.id_obat"
                        + " where id_pendaftaran='" + tidpendaftaran.getText() + "'");
                jd.setQuery(query);

                JasperReport jr = JasperCompileManager.compileReport(jd);
                JasperPrint jp = JasperFillManager.fillReport(jr, param, con);
                JasperViewer.viewReport(jp, false);

            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e);
        }

    }

