public void tampiltindakan() {
        String[] judul = {"ID", "Nama Tindakan","Tarif Tindakan"};
        model = new DefaultTableModel(judul, 0);
        tbltindakan.setModel(model);
        String sql = "SELECT * FROM tbl_tindakan"
                + " where nama_tindakan like '%" + tcari.getText() + "%'";

        try {
            rs = con.createStatement().executeQuery(sql);

            while (rs.next()) {
                String id = rs.getString("id_Tindakan");              
                String nm = rs.getString("nama_tindakan");
                String tarif = rs.getString("tarif");

                String[] data = {id,nm, tarif};
                model.addRow(data);

            }
        } catch (Exception e) {
            System.out.println(e);
            JOptionPane.showMessageDialog(null, "tabel pembayaran" + e);

        }
    }

    public void tampiltindakanpemeriksaan() {
         String[] judul = {"ID", "Nama Tindakan", "Tanggal Keluar", "Jumlah", "Tarif Penunjang"};
        modeltindakan = new DefaultTableModel(judul, 0);
        tblpemeriksaan.setModel(modeltindakan);
        String sql = "SELECT tbl_tindakan.nama_tindakan,tbl_ritindakanperawat.* FROM tbl_ritindakanperawat "
                + " inner join tbl_tindakan on tbl_tindakan.id_tindakan=tbl_ritindakanperawat.id_tindakan"
                + " where id_pendaftaran ='" + tidpendaftaran.getText() + "'";

        try {
            rs = con.createStatement().executeQuery(sql);

            while (rs.next()) {
                String id = rs.getString("id_tindakan");
                String nm = rs.getString("nama_tindakan");
                String tgl = rs.getString("tanggal_keluartindakan");
                String jml = rs.getString("jumlah_tindakan");
                String tarif = rs.getString("harga_totaltindakan");

                String[] data = {id, nm, tgl, jml, tarif};
                modeltindakan.addRow(data);

            }
        } catch (Exception e) {
            System.out.println(e);
            JOptionPane.showMessageDialog(null, "tabel pembayaran" + e);

        }
    }

    public void tanggal() {

        Calendar kal = new GregorianCalendar();
        int tahun = kal.get(Calendar.YEAR);
        int bulan = kal.get(Calendar.MONTH) + 1;
        int hari = kal.get(Calendar.DAY_OF_MONTH);

        if (bulan < 10) {
            String tanggal = tahun + "/0" + bulan + "/" + hari;
            ttanggal.setText(tanggal);
        } else {
            String tanggal = tahun + "/" + bulan + "/" + hari;

            ttanggal.setText(tanggal);
        }
    }

    public void resetform() {
        tampiltindakanpemeriksaan();
        tampiltindakan();
        btntambah.setEnabled(false);
    }
