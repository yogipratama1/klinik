    public void tanggal() {

        Calendar kal = new GregorianCalendar();
        int tahun = kal.get(Calendar.YEAR);
        int bulan = kal.get(Calendar.MONTH) + 1;
        int hari = kal.get(Calendar.DAY_OF_MONTH);

        if (bulan < 10) {
            String tanggal = tahun + "-0" + bulan + "-" + hari;
            ttanggal.setText(tanggal);
        } else {
            String tanggal = tahun + "-" + bulan + "-" + hari;

            ttanggal.setText(tanggal);
        }
    }

    public void tampilkamar() {
        String[] judul = {"ID Kamar", "Nama Kamar", "Level Kamar", "Harga Per hari", "Status Kamar"};
        modelkamar = new DefaultTableModel(judul, 0);
        tblkamar.setModel(modelkamar);
        String sql = "select * from tbl_rikamar where status_kamar='kosong' and nama_kamar like '%" + tcarikamar.getText() + "%' ";

        try {
            rs = con.createStatement().executeQuery(sql);

            while (rs.next()) {

                String id = rs.getString("id_kamar");
                String nama = rs.getString("nama_kamar");
                String level = rs.getString("level_kamar");
                String harga = rs.getString("harga_kamar");
                String status = rs.getString("status_kamar");

                String[] data = {id, nama, level, harga, status};
                modelkamar.addRow(data);
            }
        } catch (Exception e) {
            System.out.println(e);

        }

    }

