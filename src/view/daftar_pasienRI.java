public void tabelpasien() {
        String[] judul = {"ID Pendaftaran", "NO RM", "Nama Pasien", "Ruang Kamar", "JK"};
        model = new DefaultTableModel(judul, 0);
        tblpasien.setModel(model);
        String sql = "SELECT *, tbl_rm.*, tbl_ripelayanan.* from tbl_ripelayanan"
                + " inner join tbl_rm on tbl_rm.kode_rm=tbl_ripelayanan.no_rm "
                + " where status_pasien='dirawat' and nama_pasien like'%" + tcari.getText() + "%' ORDER BY id_pendaftaran DESC";

        try {
            rs = con.createStatement().executeQuery(sql);

            while (rs.next()) {
                String idpendaftaran = rs.getString("id_pendaftaran");
                String norm = rs.getString("no_rm");
                String nama = rs.getString("nama_pasien");
                String rkamar = rs.getString("nama_kamar");
                String jk = rs.getString("jenis_kelamin");

                String[] data = {idpendaftaran, norm, nama, rkamar, jk};
                model.addRow(data);

            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e);
        }

    }

