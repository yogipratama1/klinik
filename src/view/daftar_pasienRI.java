    private void tblpasienMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblpasienMouseClicked
        int i = tblpasien.getSelectedRow();
        tidpendaftaran.setText(model.getValueAt(i, 0).toString());
        tkoderm.setText(model.getValueAt(i, 1).toString());
        tnamapasien.setText(model.getValueAt(i, 2).toString());
        truangkamar.setText(model.getValueAt(i, 3).toString());

        String sql = "select * from tbl_ripelayanan where id_pendaftaran='" + tidpendaftaran.getText() + "'";
        try {
            rs = con.createStatement().executeQuery(sql);
            while (rs.next()) {
                String usia = rs.getString("usia");
                String ket = rs.getString("catatan");
                String diagnosa = rs.getString("diagnosa");
                String Jkunjungan = rs.getString("jenis_kunjungan");
                jeniskunjunganbl.setText(Jkunjungan);
                tusia.setText(usia);
                tketerangan.setText(ket);
                ticd.setText(diagnosa);

            }
        } catch (Exception e) {
            System.out.println(e);
            JOptionPane.showMessageDialog(null, "Gagal mengambil data !! " + e);
        }

        btnbatal.setEnabled(true);
        btnsimpan.setEnabled(true);


    }//GEN-LAST:event_tblpasienMouseClicked

