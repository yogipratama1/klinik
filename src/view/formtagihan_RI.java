    private void btnsimpantagihanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnsimpantagihanActionPerformed

        String kamar = tbiayakamar.getText();
        String visit = tvisitedokter.getText();
        String injeksi = tbiayaalkes.getText();
        String cairan = tbiayacairan.getText();
        String tablet = tbiayatablet.getText();
        String admin = tbiayaadmin.getText();
        String hari = tjumlahhari.getText();
        String ambulan = tbiayaambulan.getText();
        String jmlvisit = tjumlahvisite.getText();
        String tindakan = ttindakanperawat.getText();
        int persalinan = Integer.parseInt(tpersalinan.getText());
        int perawatan = Integer.parseInt(tperawatanbayi.getText());

        String penunjang = tbiayapenunjang.getText();

        if (admin.length() == 0) {
            JOptionPane.showMessageDialog(null, "Data Masih Belum Lengkap !!");

        } else {
            try {
                con.createStatement().executeUpdate("insert into tbl_ridetpembayaran values("
                        + "'" + tidpendaftaran.getText() + "','" + kamar + "','" + visit + "','" + injeksi + "','" + cairan + "','" + tablet + "','" + penunjang + "','" + admin + "',"
                        + "'" + hari + "','" + ambulan + "','" + jmlvisit + "','" + tindakan + "','" + persalinan + "','" + perawatan + "')");
                JOptionPane.showMessageDialog(null, "Berhasil !!");
                tbayar.setEnabled(true);

                jumlahtotal();
            } catch (Exception e) {
                System.out.println(e);
                JOptionPane.showMessageDialog(null, "Gagal/Tagihan Sudah Disimpan !! " + e);
            }
        }
    }//GEN-LAST:event_btnsimpantagihanActionPerformed

