    public void jmlhari() {
        String sql = "select hour(timediff(tanggal_krs,tanggal_mrs)) as jmljam from tbl_ripelayanan where id_pendaftaran='" + tidpendaftaran.getText() + "'";
        try {
            rs = con.createStatement().executeQuery(sql);
            while (rs.next()) {

                int jmljam = Integer.parseInt(rs.getString("jmljam"));
                if (jmljam >= 0 && jmljam <= 31) {
                    tjumlahhari.setText("1");
                } else if (jmljam > 31 && jmljam <= 54) {
                    tjumlahhari.setText("2");
                } else if (jmljam > 54 && jmljam <= 78) {
                    tjumlahhari.setText("3");
                } else if (jmljam > 78 && jmljam <= 102) {
                    tjumlahhari.setText("4");
                } else if (jmljam > 102 && jmljam <= 126) {
                    tjumlahhari.setText("5");
                } else if (jmljam > 126) {
                    tjumlahhari.setText("6");
                }

                String sql1 = "select tbl_rikamar.harga_kamar,tbl_rikamar.visite_dokter,tbl_rikamar.id_kamar,tbl_ripelayanan.nama_kamar from tbl_ripelayanan "
                        + " inner join tbl_rikamar using(nama_kamar)"
                        + " where id_pendaftaran='" + tidpendaftaran.getText() + "'";

                try {
                    rs = con.createStatement().executeQuery(sql1);
                    while (rs.next()) {
                        String idkamar = rs.getString("id_kamar");
                        int harga = Integer.parseInt(rs.getString("harga_kamar"));
                        int visitdokter = Integer.parseInt(rs.getString("visite_dokter"));

                        int jmlvisit = Integer.parseInt(String.valueOf(tjumlahvisite.getText()));
                        int jmlhari = Integer.parseInt(String.valueOf(tjumlahhari.getText()));
                        if (jmlhari == 0) {
                            int jumlahtotal = 1 * harga;
                            int hargavisite = jmlvisit * visitdokter;

                            tvisitedokter.setText(String.valueOf(hargavisite));
                            tbiayakamar.setText(String.valueOf(jumlahtotal));
                            tidkamar.setText(idkamar);
                            jumlahvisitedokter();
                        } else {
                            int jumlahtotal = jmlhari * harga;
                            int hargavisite = jmlvisit * visitdokter;

                            tvisitedokter.setText(String.valueOf(hargavisite));
                            tbiayakamar.setText(String.valueOf(jumlahtotal));
                            tidkamar.setText(idkamar);
                            jumlahvisitedokter();
                        }
                    }
                } catch (Exception e) {
                    System.out.println(e);
                    JOptionPane.showMessageDialog(null, "Gagall mengambil data harga kamar !!" + e);
                }
            }
        } catch (Exception e) {
            System.out.println(e);
            JOptionPane.showMessageDialog(null, "Gagal mengambil data jumlah hari !!" + e);

        }

    }

