public void tampilpelayanan() {
        String[] judul = {"ID Pendaftaran", "Kode RM", "Nama Pasien", "JK", "Alamat", "Umur", "J.Kunjungan", "Krit. Kunjungan", "Poli", "Petugas Periksa", "Kode ICD", "Umum/BPJS", "Catatan/Keterangan"};
        model = new DefaultTableModel(judul, 0);
        tbltransaksi.setModel(model);
        String sql = "SELECT *,tbl_rm.*,tbl_pelayanan.* from tbl_pendaftaran"
                + " inner join tbl_pelayanan using(id_pendaftaran)"
                + " inner join tbl_rm on tbl_rm.kode_rm = tbl_pelayanan.kode_rm "
                + " ORDER BY tbl_pendaftaran.id_pendaftaran DESC";
        lebarkolom();

        try {
            rs = con.createStatement().executeQuery(sql);

            while (rs.next()) {
                String id = rs.getString("id_pendaftaran");
                String kode = rs.getString("kode_rm");
                String nama = rs.getString("nama_pasien");
                String alamat = rs.getString("alamat");
                String umur = rs.getString("umur");
                String krit = rs.getString("kriteria_pengunjung");
                String poli = rs.getString("poli");
                String petugas = rs.getString("petugas_periksa");
                String kodeicd = rs.getString("kode_icd");
                String umum = rs.getString("umum_bpjs");
                String ket = rs.getString("keterangan");
                String JenisK = rs.getString("jenis_kunjungan");
                String jk = rs.getString("jenis_kelamin");

                String[] data = {id, kode, nama, jk, alamat, umur, JenisK, krit, poli, petugas, kodeicd, umum, ket};
                model.addRow(data);
            }
        } catch (Exception e) {
            System.out.println(e);

        }
    }

    public void tanggal() {

        Calendar kal = new GregorianCalendar();
        int tahun = kal.get(Calendar.YEAR);
        int bulan = kal.get(Calendar.MONTH) + 1;
        int hari = kal.get(Calendar.DAY_OF_MONTH);

        if (bulan < 10) {
            String tanggal = tahun + "/0" + bulan + "/" + hari;
            ttanggal.setText(tanggal);
        } else {
            String tanggal = tahun + "/" + bulan + "/" + hari;

            ttanggal.setText(tanggal);
        }
    }

