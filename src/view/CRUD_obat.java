        cbkategori1.setSelectedItem(model.getValueAt(i, 5));
        tnamaobat.setText(model.getValueAt(i, 3).toString());
        thargabeli.setText(model.getValueAt(i, 5).toString());
        thargajual.setText(model.getValueAt(i, 6).toString());
