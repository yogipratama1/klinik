    public void combokategori() {

        String sql = "SELECT * FROM tbl_kategoriobat ";

        try {
            PreparedStatement pst = con.prepareStatement(sql);
            rs = pst.executeQuery();
            while (rs.next()) {
                cbkategori1.addItem(rs.getString("kategori"));
                cbkategoriobat.addItem(rs.getString("kategori"));

            }
        } catch (Exception e) {
        }
    }

    public void tanggal() {

        Calendar kal = new GregorianCalendar();
        int tahun = kal.get(Calendar.YEAR);
        int bulan = kal.get(Calendar.MONTH) + 1;
        int hari = kal.get(Calendar.DAY_OF_MONTH);

        if (bulan < 10) {
            String tanggal = tahun + "-0" + bulan + "-" + hari;
            ttanggal.setText(tanggal);
        } else {
            String tanggal = tahun + "-" + bulan + "-" + hari;

            ttanggal.setText(tanggal);
        }
    }

    public void updatestok() {
        try {
            String nobatch = tnobatch.getText();
            DateFormat df = new SimpleDateFormat("yyyy-MM-dd");
            String exdate = df.format(tkadaluarsa.getDate());
            String nama = tnamaobat.getText();

            String merek = tmerekobat.getText();
            String HB = thargabeli.getText();
            String HJ = thargajual.getText();
            int stokbaru = Integer.parseInt(tstok.getText());
            int stoklama = Integer.parseInt(model.getValueAt(tblobat.getSelectedRow(), 7).toString());
            int stok = stokbaru + stoklama;
            con.createStatement().executeUpdate("update tbl_obat set nama_obat='" + nama + "',no_batch='" + nobatch + "',ex_date='" + exdate + "',merek_obat='" + merek + ""
                    + "',harga_beli='" + HB + "',harga_jual='" + HJ + "',stok_obat='+" + stok + "' "
                    + "where id_obat ='" + model.getValueAt(tblobat.getSelectedRow(), 0) + "'");
            con.createStatement().executeUpdate("Update tbl_laporanobatbulanan set stok_awal='" + stok + "' where id_obat='" + model.getValueAt(tblobat.getSelectedRow(), 0) + "'");
            JOptionPane.showMessageDialog(null, "Data Berhasil Diubah");
            Resetform();

        } catch (Exception e) {
            System.out.println(e);
            JOptionPane.showMessageDialog(null, e);
        }
    }

    public void lebarkolom() {
        TableColumn column;
        for (int i = 0; i < 8; i++) {
            column = tblobat.getColumnModel().getColumn(i);
            if (i == 0) {
                column.setPreferredWidth(10);
            } else if (i == 1) {
                column.setPreferredWidth(50);
            } else if (i == 2) {
                column.setPreferredWidth(50);
            } else if (i == 3) {
                column.setPreferredWidth(180);
            } else if (i == 4) {
                column.setPreferredWidth(100);
            }
        }
    }
