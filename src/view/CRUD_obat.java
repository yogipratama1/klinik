public void tabelobat() {
        String[] judul = {"ID Obat", "No Batch", "EX Date", "Nama Obat", "Merek Obat", "Harga Beli", "Harga Jual", "Sisa Stok"};
        model = new DefaultTableModel(judul, 0);
        tblobat.setModel(model);
        String sql = "SELECT * FROM tbl_obat where kategori_obat='" + cbkategoriobat.getSelectedItem() + "'and (nama_obat like '%" + tCari.getText() + "%' or merek_obat like'%" + tCari.getText() + "%') ";
        lebarkolom();

        try {
            rs = con.createStatement().executeQuery(sql);

            while (rs.next()) {
                String id = rs.getString("id_obat");
                String nobatch = rs.getString("no_batch");
                String exdate = rs.getString("ex_date");
                String nama = rs.getString("nama_obat");
                String merek = rs.getString("merek_obat");

                String HB = rs.getString("harga_beli");
                String HJ = rs.getString("harga_jual");
                String stok = rs.getString("stok_obat");

                String[] data = {id, nobatch, exdate, nama, merek, HB, HJ, stok};
                model.addRow(data);
            }
        } catch (Exception e) {
            System.out.println(e);

        }
    }

